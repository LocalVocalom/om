package com.vocal.viewmodel;

public class StatusDto {
	
	private String status;
	
	public StatusDto() {
		super();
	}
	
	public StatusDto(String status) {
		super();
		this.status = status;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}
}
